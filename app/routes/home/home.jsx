export const Home = () => {
  const [visibleSections, setVisibleSections] = useState([]);
  const [scrollIndicatorHidden, setScrollIndicatorHidden] = useState(false);
  const intro = useRef();
  const projectOne = useRef();
  const projectTwo = useRef();
  const projectThree = useRef();
  const details = useRef();

  useEffect(() => {
    const sections = [intro, projectOne, projectTwo, projectThree, details];

    const sectionObserver = new IntersectionObserver(
      (entries, observer) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const section = entry.target;
            observer.unobserve(section);
            if (visibleSections.includes(section)) return;
            setVisibleSections(prevSections => [...prevSections, section]);
          }
        });
      },
      { rootMargin: '0px 0px -10% 0px', threshold: 0.1 }
    );

    const indicatorObserver = new IntersectionObserver(
      ([entry]) => {
        setScrollIndicatorHidden(!entry.isIntersecting);
      },
      { rootMargin: '-100% 0px 0px 0px' }
    );

    sections.forEach(section => {
      sectionObserver.observe(section.current);
    });

    indicatorObserver.observe(intro.current);

    return () => {
      sectionObserver.disconnect();
      indicatorObserver.disconnect();
    };
  }, [visibleSections]);

  return (
    <div className={styles.home}>
      <Intro
        id="intro"
        sectionRef={intro}
        scrollIndicatorHidden={scrollIndicatorHidden}
      />
      <ProjectSummary
        id="project-1"
        sectionRef={projectOne}
        visible={visibleSections.includes(projectOne.current)}
        index={1}
        title="Movie Recommendation System"
        description="A Movie Recommendation System built using Python. Provides personalized movie suggestions based on user preferences and collaborative filtering."
  buttonText="View repository"
  buttonLink="https://github.com/priti275/Movie-Recommendation-System-Using-Python"
        model={{
          type: 'image',
          alt: 'Movie Recommendation System UI',
          textures: [
            {
              // Local asset: movie.png (place the file at app/assets/movie.png)
              srcSet: `${movieImage} 1280w, ${movieImage} 2560w`,
              placeholder: movieImage,
            },
          ],
        }}
      />
      <ProjectSummary
        id="project-2"
        alternate
        sectionRef={projectTwo}
        visible={visibleSections.includes(projectTwo.current)}
        index={2}
        title="Product Detection System"
        description="A Flipkart Grid 6.0 finalist project that uses computer vision and deep learning to automatically detect and classify products in images"
  buttonText="View repository"
  buttonLink="https://github.com/SkDevilS/Leetcoders-Flipkart-Grid-6.0"
        model={{
          type: 'image',
          alt: 'Product Detection System Demo',
          textures: [
            {
              srcSet: `${productImage} 800w, ${productImage} 1440w`,
              placeholder: productImage,
            },
          ],
        }}
      />
      <ProjectSummary
        id="project-3"
        sectionRef={projectThree}
        visible={visibleSections.includes(projectThree.current)}
        index={3}
        title="Smart Bus Tracking System"
        description="A real-time IoT-based Smart Bus Tracking System that provides live location updates, ETA predictions, and route monitoring for passengers and operators."
  buttonText="View repository"
  buttonLink="https://github.com/priti275/"
        model={{
          type: 'image',
          alt: 'Smart Bus Tracking System Dashboard',
          textures: [
            {
              srcSet: `${busImage} 800w, ${busImage} 1440w`,
              placeholder: busImage,
            },
          ],
        }}
      />
      <Profile
        sectionRef={details}
        visible={visibleSections.includes(details.current)}
        id="details"
      />
      <Footer />
    </div>
  );
};
